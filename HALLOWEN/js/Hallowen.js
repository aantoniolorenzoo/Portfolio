function show(spider,ghosthorse,witch,arañaMovimiento){
    document.getElementById(spider,ghosthorse,witch,arañaMovimiento).style.visibility="visible";
    /* extrae  con (get) un elemento (pinkfire) y cambia algo en su estilo (visibiliadad) de manera que pasa de estar oculto a visible*/
}
function hide(spider,ghosthorse,witch){
    document.getElementById(spider,ghosthorse,witch,arañaMovimiento,).style.visibility="hidden";
}
